export function getCardRubrics(q) {
  const splits = getFactSplit(q);
  const rubrics = [];

  // 1. Date/Year
  if (q.year && q.year > 0) {
    rubrics.push({
      label: 'Date/Year',
      text: String(q.year)
    });
  } else {
    const yearMatch = (q.question + " " + q.explanation).match(/\b(19\d{2})\b/);
    if (yearMatch) {
      rubrics.push({
        label: 'Date/Year',
        text: yearMatch[1]
      });
    } else {
      rubrics.push({
        label: 'Core Concept',
        text: q.answer
      });
    }
  }

  // 2. Context
  let contextText = splits.context || '';
  contextText = contextText.replace(/[\*_]/g, '');
  rubrics.push({
    label: 'Context',
    text: contextText
  });

  // 3. Significance
  let significanceText = splits.significance || '';
  significanceText = significanceText.replace(/[\*_]/g, '');
  rubrics.push({
    label: 'Significance',
    text: significanceText
  });

  return rubrics;
}
